class Paper extends StatelessWidget {
  const Paper({super.key});

  @override
  Widget build(BuildContext context) {
    return Container(
      color: Colors.white,
      child: CustomPaint(
        // 使用CustomPaint
        painter: PaperPainter(),
      ),
    );
  }
}

class PaperPainter extends CustomPainter {
  final Coordinate coordinate = Coordinate();

  late Paint _paint;

  final double strokeWidth = 0.5;
  final Color color = Colors.blue;

  PaperPainter() {
    _paint = Paint()
      ..style = PaintingStyle.fill
      ..strokeWidth = strokeWidth
      ..color = color;
  }

  @override
  void paint(Canvas canvas, Size size) {
    coordinate.paint(canvas, size);
    canvas.translate(size.width / 2, size.height / 2);

    // canvas.save();
    var rect = Rect.fromCenter(center: Offset.zero,width: 360,height: 240);
    canvas.clipRect(rect,doAntiAlias: true,clipOp: ui.ClipOp.intersect);

    var colors = [
      Color(0xFFF60C0C),
      Color(0xFFF3B913),
      Color(0xFFE7F716),
      Color(0xFF3DF30B),
      Color(0xFF0DF6EF),
      Color(0xFF0829FB),
      Color(0xFFB709F4),
    ];

    var pos = [1.0 / 7, 2.0 / 7, 3.0 / 7, 4.0 / 7, 5.0 / 7, 6.0 / 7, 1.0];

    _paint.shader = ui.Gradient.linear(
        rect.centerLeft, rect.centerRight,
        colors, pos, TileMode.clamp);

    _paint.blendMode=BlendMode.lighten;

    canvas.drawPaint(_paint);
    // canvas.restore();
    // canvas.drawColor(Colors.blue, BlendMode.lighten);
  }

  @override
  bool shouldRepaint(CustomPainter oldDelegate) => false;
}
